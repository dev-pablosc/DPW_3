// Existen 3 formas de declarar una variable => (var, let, const)

var saludo = "Hola Mundo"; // ya no es tan usado, funciona igual que 'let'
let saludo2 = "Hola Mundo con let"; // declarado con let el valor puede cambiar despues
const saludo3 = "Hola Mundo con const"; // declarando con const el valor no puede ser modificado despues

// las variables se pueden declarar VACIAS, sin valor
let nombre = ''; // declarando un string vacio para usarlo despues
nombre = 'Pablo' // asignamos el valor (no volvemos a declarar la variable 'let' esta la declaramos en la linea 8)

// las variables con 'let' o 'var' se pueden REASIGNAR
nombre = 'David';

