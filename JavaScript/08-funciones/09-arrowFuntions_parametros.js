// pasar parametros en arrow functions
const aprendiendo = function(idioma){
    console.log(`Estoy aprendiendo ${idioma}`)
}
aprendiendo('Italiano')

// arrow function
const aprendiendo2 = (idioma) => {
    console.log(`Estoy aprendiendo ${idioma}`)
}
aprendiendo2('Frances')

const aprendiendo3 = (x) => console.log(`Estoy aprendiendo ${x}`)
aprendiendo3('Aleman')